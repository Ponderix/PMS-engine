//hidden divs
var helpButtonDiv = document.getElementById("help_box")

function helpButton() {
  if (helpButtonDiv.style.display === "block") {
    helpButtonDiv.style.display = "none"
  } else {
    helpButtonDiv.style.display = "block"
  }
}

var cpckBtnDiv = document.getElementById("colorpicker_box")
var cpckBtnID = document.getElementById("colour_picker_btn")

function cpckBtn() {
  if (cpckBtnDiv.style.display === "none") {
    cpckBtnDiv.style.display = "block"
  } else {
    cpckBtnDiv.style.display = "none"
  }
  if (cpckBtnDiv.style.display === "none") {
    cpckBtnID.style.background = "#203040"
  } else {
    cpckBtnID.style.background = "green"
  }
}

